public class maxof3 {
	
	public static void main (String[] args) {
		int x = IBIO.inputInt("No. 1: ");
		int y = IBIO.inputInt("No. 2: ");
		int z = IBIO.inputInt("No. 3: ");
		int max = 0;
		if (x>max)
		{
			max=x;
		}
		if (y>max)
		{
			max=y;
		}
		if (z>max)
		{
			max=z;
		}
		IBIO.output(max);
	}
}

